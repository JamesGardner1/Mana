package com.james;

public class Magic {
    String spell;
    int baseDamage;
    int maxDamage;
    int manaCost;

    public Magic(String spell, int baseDamage, int maxDamage, int manaCost) {
        this.spell = spell;
        this.baseDamage = baseDamage;
        this.maxDamage = maxDamage;
        this.manaCost = manaCost;
    }









}
