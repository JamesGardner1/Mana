package com.james;


public class Enemy {
    String name;
    int hp;
    int baseDamage;
    int maxDamage;
    int expWorth;

    public Enemy(String name, int hp, int baseDamage, int maxDamage, int expWorth) {
        this.name = name;
        this.hp = hp;
        this.baseDamage = baseDamage;
        this.maxDamage = maxDamage;
        this.expWorth = expWorth;
    }






}
