package com.company;

public class Player {
    String name;
    int lvl;
    int exp;
    int hp;
    int mana;
    int damage;

    public Player(String name, int lvl, int exp, int hp, int mana, int damage) {
        this.name = name;
        this.lvl = lvl;
        this.exp = exp;
        this.hp = hp;
        this.mana = mana;
        this.damage = damage;
    }




}
